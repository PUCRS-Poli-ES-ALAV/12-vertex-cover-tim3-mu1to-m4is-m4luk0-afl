public class Node {

    private String id;
    private int value;

    public Node(String id, int value){
        this.id = id;
        this.value = value;
    }

    public String getId() {
        return id;
    }

    public int getValue() {
        return value;
    }

    @Override
    public String toString() {
        return "Node [id=" + id + ", value=" + value + "]";
    } 
}
